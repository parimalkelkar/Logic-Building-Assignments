#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Name: Display()
//  Description: Displays a pattern recursively.
//  Input: void
//  Output: void
//  Author: Parimal Kelkar
//  Date: 21/07/2025
//
////////////////////////////////////////////////////////////////////////////////

void Display()
{
    static int i = 5;

    if(i > 0)
    {
        printf("%d\t", i);
        i--;
        Display();
    }
}

int main()
{
    Display();

    return 0;
}